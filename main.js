//  solicite um número ao usuário
const num1 = Number(prompt("Informe o primeiro número"));

// depois, solicite outro número
const num2 = Number(prompt("Informe o segundo número"));

// mostre em console os seguintes:
// resultado da soma do num1 com num2
const resultadoSoma = num1 + num2;
console.log("O resultado da soma é: ", resultadoSoma);

// resultado da multiplicação do num1 com num2
const resultadoMult = num1 * num2;
console.log("Resultado mult é: ", resultadoMult);

// resultado da subtração do num1 com num2
const resultadoDiv = num1 / num2;
console.log("Resultado div é: ", resultadoDiv);
